"""
Data I/O Manager for Transport Simulations
===========================================
Manages structured reading and writing of simulation data,
inspired by tec_io (project 1197).

Provides:
- Structured text output for simulation parameters and results
- Data file parsing utilities
- Formatted matrix and vector printing
- Zone-based data organization (similar to Tecplot format)

All output is text-based and non-visual.
"""

import numpy as np
import os


class IOManager:
    """
    Handles data input/output for the transport simulation.
    """

    def __init__(self, output_dir="."):
        self.output_dir = output_dir
        os.makedirs(output_dir, exist_ok=True)

    def write_simulation_header(self, filename, params):
        """
        Write simulation parameters to a structured text file.

        Parameters
        ----------
        filename : str
        params : dict
            Dictionary of simulation parameters.
        """
        filepath = os.path.join(self.output_dir, filename)
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write("# ==========================================================\n")
            f.write("# Topological Insulator Surface State Transport Simulation\n")
            f.write("# ==========================================================\n")
            f.write(f"# Generated by sci-project-synthesis-python\n")
            f.write("#\n")
            for key, value in params.items():
                if isinstance(value, float):
                    f.write(f"{key:30s} = {value:18.10e}\n")
                elif isinstance(value, int):
                    f.write(f"{key:30s} = {value:18d}\n")
                else:
                    f.write(f"{key:30s} = {str(value):18s}\n")
            f.write("# ==========================================================\n")

    def write_matrix(self, filename, matrix, title="Matrix", fmt="%.10e"):
        """
        Write a matrix to a text file with formatting.

        Parameters
        ----------
        filename : str
        matrix : ndarray
        title : str
        fmt : str
        """
        filepath = os.path.join(self.output_dir, filename)
        np.savetxt(filepath, matrix, fmt=fmt, header=title, comments='# ')

    def write_vector(self, filename, vector, title="Vector", fmt="%.10e"):
        """
        Write a vector to a text file.

        Parameters
        ----------
        filename : str
        vector : ndarray
        title : str
        fmt : str
        """
        filepath = os.path.join(self.output_dir, filename)
        np.savetxt(filepath, vector, fmt=fmt, header=title, comments='# ')

    def write_zone_data(self, filename, title, variables, data_dict):
        """
        Write zone-structured data similar to Tecplot format.

        Parameters
        ----------
        filename : str
        title : str
        variables : list
            List of variable names.
        data_dict : dict
            Dictionary mapping variable names to 1D arrays.
        """
        filepath = os.path.join(self.output_dir, filename)
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(f'TITLE = "{title}"\n')
            f.write(f'VARIABLES = "{""",""".join(variables)}"\n')
            f.write(f'ZONE T="{title}"\n')

            n_points = len(data_dict[variables[0]])
            f.write(f'I={n_points}, F=POINT\n')

            for i in range(n_points):
                row = [data_dict[var][i] for var in variables]
                f.write(' '.join([f'{v:18.10e}' for v in row]) + '\n')

    def parse_variable_line(self, line):
        """
        Parse a variable definition line.

        Inspired by tec_variable_line_parse (project 1197).

        Parameters
        ----------
        line : str

        Returns
        -------
        variables : list
        """
        line = line.strip()
        if line.upper().startswith('VARIABLES'):
            # Extract quoted strings
            vars_list = []
            in_quote = False
            current = ''
            for c in line:
                if c == '"':
                    if in_quote and current:
                        vars_list.append(current.strip())
                        current = ''
                    in_quote = not in_quote
                elif in_quote:
                    current += c
            return vars_list
        return []

    def read_zone_data(self, filename):
        """
        Read zone-structured data from a file.

        Parameters
        ----------
        filename : str

        Returns
        -------
        data_dict : dict
            Dictionary of variable arrays.
        """
        filepath = os.path.join(self.output_dir, filename)
        if not os.path.exists(filepath):
            return {}

        with open(filepath, 'r', encoding='utf-8') as f:
            lines = f.readlines()

        variables = None
        data_start = 0
        for i, line in enumerate(lines):
            if line.upper().startswith('VARIABLES'):
                variables = self.parse_variable_line(line)
            if 'F=POINT' in line.upper() or 'F=BLOCK' in line.upper():
                data_start = i + 1
                break

        if variables is None:
            return {}

        data = {var: [] for var in variables}
        for line in lines[data_start:]:
            line = line.strip()
            if not line or line.startswith('#'):
                continue
            parts = line.split()
            if len(parts) >= len(variables):
                for j, var in enumerate(variables):
                    try:
                        data[var].append(float(parts[j]))
                    except ValueError:
                        data[var].append(parts[j])

        return {k: np.array(v) for k, v in data.items()}

    def write_transport_results(self, filename, results):
        """
        Write transport coefficient results.

        Parameters
        ----------
        filename : str
        results : dict
            Dictionary of results.
        """
        filepath = os.path.join(self.output_dir, filename)
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write("# ==========================================================\n")
            f.write("# Transport Coefficients Results\n")
            f.write("# ==========================================================\n")
            for key, value in results.items():
                if isinstance(value, (list, np.ndarray)):
                    f.write(f"\n{key}:\n")
                    for v in value:
                        f.write(f"  {v:18.10e}\n")
                elif isinstance(value, float):
                    f.write(f"{key:40s} = {value:18.10e}\n")
                else:
                    f.write(f"{key:40s} = {str(value)}\n")
            f.write("# ==========================================================\n")

    def print_matrix_transpose(self, matrix, title="", max_rows=10, max_cols=10):
        """
        Pretty-print a matrix in transposed layout.

        Inspired by r8mat_transpose_print from project 1197.

        Parameters
        ----------
        matrix : ndarray
        title : str
        max_rows, max_cols : int
        """
        print(f"\n{title}")
        m, n = matrix.shape
        rows_to_print = min(m, max_rows)
        cols_to_print = min(n, max_cols)

        for i in range(rows_to_print):
            row_str = " ".join([f"{matrix[i, j]:12.6e}" for j in range(cols_to_print)])
            print(f"  Row {i:3d}: {row_str}")

        if m > max_rows:
            print(f"  ... ({m - max_rows} more rows)")
        if n > max_cols:
            print(f"  ... ({n - max_cols} more columns)")
