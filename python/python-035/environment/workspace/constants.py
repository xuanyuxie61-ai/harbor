import numpy as np

# ============================================================
# 基本物理常数 (PDG 2024 推荐值)
# ============================================================
M_HIGGS = 125.10          # 希格斯玻色子质量 [GeV]
M_Z = 91.1876             # Z 玻色子质量 [GeV]
GAMMA_Z = 2.4952          # Z 玻色子总宽度 [GeV]
GAMMA_H = 4.07e-3         # 希格斯玻色子总宽度 [GeV]
ALPHA_EM = 1.0 / 137.036  # 精细结构常数
G_F = 1.1663787e-5        # 费米常数 [GeV^-2]
SIN2THETA_W = 0.23121     # 弱混合角 sin^2(theta_W)
E_CHARGE = np.sqrt(4.0 * np.pi * ALPHA_EM)  # 元电荷 (自然单位)

# ============================================================
# 耦合常数
# ============================================================
def g_weak():
    """弱耦合常数 g = 2 * M_W / v, 其中 v = (sqrt(2) * G_F)^(-1/2) ~ 246 GeV"""
    v = 1.0 / np.sqrt(np.sqrt(2.0) * G_F)
    return 2.0 * (M_Z * np.sqrt(1.0 - SIN2THETA_W)) / v

def yukawa_tau():
    """tau 轻子汤川耦合 y_tau = m_tau / v"""
    m_tau = 1.77686  # GeV
    v = 1.0 / np.sqrt(np.sqrt(2.0) * G_F)
    return m_tau / v

# ============================================================
# 运动学边界
# ============================================================
def kine_bounds():
    """
    H -> ZZ* -> 4l 相空间边界
    对于四轻子末态，不变质量范围:
      m_4l in [4*m_l, m_H]
      每个双轻子对的不变质量 m_ll in [2*m_l, m_H - 2*m_l]
    """
    m_l = 0.000511  # 电子质量 [GeV] (作为轻子质量下限)
    m_ll_min = 2.0 * m_l
    m_ll_max = M_HIGGS - 2.0 * m_l
    m_4l_min = 4.0 * m_l
    m_4l_max = M_HIGGS
    return {
        "m_l": m_l,
        "m_ll_min": m_ll_min,
        "m_ll_max": m_ll_max,
        "m_4l_min": m_4l_min,
        "m_4l_max": m_4l_max,
    }

# ============================================================
# Breit-Wigner 传播子参数
# ============================================================
def breit_wigner_params():
    """
    Z 传播子参数:
      P_Z(q^2) = 1 / (q^2 - M_Z^2 + i * M_Z * Gamma_Z)
    运行宽度修正: Gamma_Z(s) = Gamma_Z * (s / M_Z^2) 对于费米子末态
    """
    return {
        "m_z": M_Z,
        "gamma_z": GAMMA_Z,
        "m_h": M_HIGGS,
        "gamma_h": GAMMA_H,
    }

