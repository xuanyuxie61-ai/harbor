def integrate_tetrahedron(f, scale=1.0, translate=np.zeros(3)):
    """
    使用 Felippa 24 点规则在仿射变换后的四面体上积分函数 f(x,y,z)。

    雅可比行列式 |J| = scale^3。

    参数:
      f: 接受 (N,3) 数组的函数
      scale: 缩放因子
      translate: 平移向量

    返回:
      积分近似值
    """
    w, xyz = tetrahedron_unit_o24()
    pts = xyz * scale + translate
    vals = f(pts)
    jac = scale ** 3
    return np.sum(w * vals) * jac


